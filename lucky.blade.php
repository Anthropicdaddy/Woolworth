<script>
    function StartLucky(){
        loading();
        $.ajaxSetup({
            headers: {
                'X-CSRF-TOKEN': '{{csrf_token()}}'
            }
        });

        var code = document.querySelector('input[name="code"]').value;
        if (code == ''){
            loadingOff()
            message('Enter AI reword code.');
            return 0;
        }

        $.ajax({
            url: "{{url('submit-bonus-amount')}}"+"/"+code,
            type: 'get',
            dataType: 'json',
            success: function(res) {
                document.querySelector('input[name="code"]').value = '';
                loadingOff()
                if (res.status == true){
                    msg('Successful')

                    setTimeout(function (){
                        msgOff()
                    }, 500)
                }else {
                    message(res.error);
                }
            }
        });
    }
